--Cria o Banco
CREATE DATABASE EMPRESA

--Scripts DDL
USE EMPRESA

CREATE TABLE LOCATION (
	ID INT NOT NULL IDENTITY PRIMARY KEY,
	NAME NVARCHAR(MAX),
LATITUDE NVARCHAR(MAX) NOT NULL,
	LONGITUDE NVARCHAR(MAX) NOT NULL,
	CREATED_AT DATETIME DEFAULT CURRENT_TIMESTAMP
);



