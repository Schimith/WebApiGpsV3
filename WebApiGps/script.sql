USE EMPRESA
--Trigger para atualização automática do campo created_at após update
CREATE TRIGGER trgAfterUpdate ON LOCATION
AFTER UPDATE 
AS
  UPDATE L set CREATED_AT=GETDATE() 
  FROM 
  LOCATION AS L 
  INNER JOIN inserted
  AS i 
  ON L.ID = i.ID;
